
print("\nCalcular a média entre dois números!!")

nt1 = float(input("\nDigite a primeira nota: "))
nt2 = float(input("Digite a segunda nota: "))

media = (nt1 + nt2) / 2

if (media >= 7):
    print("\nVocê foi aprovado!")

else:
    print("\nVocê foi reprovado!!")


print("\nSua média foi: ", media, "\n")    
